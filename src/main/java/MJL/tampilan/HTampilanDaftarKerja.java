    private void TombolSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TombolSimpanActionPerformed
        String[] data_regis = new String[12];
        String username = Login.login_user;
        data_regis[0] = Tnama.getText();
        data_regis[1] = Ttempatlahir.getText();
        data_regis[2] = String.valueOf(Dtanggallahir.getDate());
        if (Rpria.isSelected()){
            data_regis[3] = "Laki-laki";
        } else if (Rwanita.isSelected()) {
            data_regis[3] = "Wanita";
        }
        data_regis[4] = Talamat.getText();
        data_regis[5] = (String) Pagama.getSelectedItem();
        data_regis[6] = (String) Ppernikahan.getSelectedItem();
        data_regis[7] = Tpengalaman.getText();
        data_regis[8] = (String) Plulusan.getSelectedItem();
        data_regis[9] = Ttelepon.getText();
        data_regis[10] = (String) Pperusahaan.getSelectedItem();
        data_regis[11] = Tfoto.getText();
        pendaftaran.daftar_kerja(username, data_regis);
    }//GEN-LAST:event_TombolSimpanActionPerformed
    
