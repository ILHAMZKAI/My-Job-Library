    private void TombolTelusuriActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TombolTelusuriActionPerformed
        JFileChooser browseImageFile = new JFileChooser();
        FileNameExtensionFilter fnef = new FileNameExtensionFilter("IMAGES", "png", "jpg", "jpeg");
        browseImageFile.addChoosableFileFilter(fnef);
        int showOpenDialogue = browseImageFile.showOpenDialog(null);
 
        if (showOpenDialogue == JFileChooser.APPROVE_OPTION) {
            File selectedImageFile = browseImageFile.getSelectedFile();
            selectedimg = selectedImageFile.getAbsolutePath();
            Tfoto.setText(selectedimg);
            ImageIcon img = new ImageIcon(selectedimg);
            Image image = img.getImage().getScaledInstance(LabelFoto.getWidth(), LabelFoto.getHeight(), Image.SCALE_SMOOTH);
            LabelFoto.setIcon(new ImageIcon(image));
        }
        
    }//GEN-LAST:event_TombolTelusuriActionPerformed

